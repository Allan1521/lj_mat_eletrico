    <h3>Fitas Alta fusão</h3>
    <p>Descrição do Fitas Alta fusão</p>
    <p>A Fita Isolante de Auto fusão é ideal para o isolamento elétrico de altas tensões, incluindo isolação de cabos de até 69KV que possam alcançar a temperatura de 90°C. Oferece proteção mecânica e vedação contra umidade, além de contar com grande potencial de alongamento para proporcionar isolação inviolável.</p>
