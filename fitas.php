    <h3>Fitas isolantes</h3>
    <p>Descrição do Fitas isolantes</p>
    <p>Ideal para efetuar isolação elétrica para fios e cabos de energia de baixa tensão, a fim de formar uma capa protetora altamente resistente à abrasão, e também contra ação dos raios Ultravioleta. É uma Fita para isolação elétrica de uso geral em instalações de até 600V e com temperatura entre 0 A 80°C.</p>
