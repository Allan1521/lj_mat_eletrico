    <h3>Fitas isolantes coloridas</h3>
    <p>Descrição do Fitas isolantes coloridas</p>
    <p>As fitas coloridas são utilizadas na identificação dos condutores em instalações elétricas de baixa ou média tensão. Com isso a necessidade de selecionar fios ou cabos com cor específica fica de lado.</p>
</div>
