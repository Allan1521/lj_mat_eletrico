    <h3>Cabos PP de </h3>
    <p>Descrição do Cabos PP's </p>
    <p>
        Algumas opções mais comumente usadas são: cabo PP 2x2,5 (para ligação de aparelhos elétricos em geral, como eletrodomésticos, ferramentas motorizadas e extensões), cabo PP 2x1,5 (mais usado para extensões elétricas), cabo PP 4 mm (usado em instalações industriais e residenciais de força e de luz) e cabo PP 6 mm (cabo geral de iluminação).

        Para definir corretamente a bitola, você ainda deverá conferir outros dois pontos: a potência e a corrente elétrica (medida em Ampère). Por exemplo, um cabo de 1,5 mm² suporta correntes de até 15,5 A sem aquecer; já um cabo de bitola de 6 mm² suporta correntes de 36 A.

        Por fim, avalie onde será realizada a instalação e se os cabos ficarão embutidos ou externos. Estes últimos precisam de cuidados especiais e de reforço para suportarem as condições do ambiente.</p>
