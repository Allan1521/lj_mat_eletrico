    <h3>Cabos flexíveis</h3>
    <p>Descrição do Cabos flexíveis</p>
    <p>Os cabos elétricos são formados por vários fios entrelaçados, tornando-o muito mais flexível e com muito mais dificuldade para se romper quando comparado ao fio rigído. Sua capacidade de condução de corrente elétrica é proporcional ao seu tamanho em milímetros.
    As bitolas de fios e cabos podem variar de 1,5 mm² até 500 mm². Veja as medidas mais comuns: Fios: bitola máxima de 10 mm². Cabos flexíveis: bitolas de até 25 mm².</p>
