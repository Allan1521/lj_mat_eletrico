    <h3>Cabos PP</h3>
    <p>Descrição do Cabos PP</p>
    <p>O cabo PP tem esse nome porque é formado por duas capas de PVC, uma dentro da outra. De maneira geral, qualquer cabo com dupla isolação ou multipolar é considerado um cabo PP, pois é um cabo com duas ou mais pontas, em apenas uma.
    O cabo PP 3 vias agrega, num único cabo, três vias condutoras de energia, cada qual identificada por uma cor diferente (em geral preta, azul e marrom). Esse produto é bastante empregado como cabo de alimentação de equipamentos e máquinas, em que o terceiro condutor tem como finalidade o aterramento.
</p>
